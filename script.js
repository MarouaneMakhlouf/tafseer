function getData(i) {
    // Use Promise.all to wait for both axios requests to complete
    Promise.all([
        axios.get("./quran.json"),
        axios.get("./tafseer.json")
    ]).then(([qurand, tafsird]) => {
        // Call showData after both promises are resolved
        showData(i, qurand, tafsird);
    }).catch(error => {
        console.error("Error fetching data", error);
    });
}


